    void removerow() {
        DefaultTableModel dm = (DefaultTableModel) jTable1.getModel();
        int rowCount = dm.getRowCount();
//Remove rows one by one from the end of the table
        for (int i = rowCount - 1; i >= 0; i--) {
            dm.removeRow(i);
        }
    }
    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        Result_show show = new Result_show();
        show.setVisible(true);
        DefaultTableModel model = (DefaultTableModel) show.resutl_show_table.getModel();
        DefaultTableModel m = (DefaultTableModel) jTable1.getModel();
        for (int i = 0; i < m.getRowCount(); i++) {
            if (jComboBox5.getSelectedItem().equals(m.getValueAt(i, 2))) {
                Object id = m.getValueAt(i, 0);
                Object name = m.getValueAt(i, 1);
                Object department = m.getValueAt(i, 2);
                int sb1 = (int) m.getValueAt(i, 3);
                int sb2 = (int) m.getValueAt(i, 4);
                int sb3 = (int) m.getValueAt(i, 5);
                int sb4 = (int) m.getValueAt(i, 6);
                int ave = (sb1 + sb2 + sb3 + sb4) / 4;
                Object average = (Object) ave;
                Object result = "";
                if (ave > 45) {
                    result = "2nd Class";
                } else if (ave > 60) {
                    result = "1st Cass";
                } else {
                    result = "3rd Cass";
                }
                model.addRow(new Object[]{id, name, department, average, result});
                show.Result_Label.setText(jComboBox5.getSelectedItem().toString());

            } else {
                show.Result_Label.setText(jComboBox5.getSelectedItem().toString());
            }
        }
    }//GEN-LAST:event_jButton1ActionPerformed

