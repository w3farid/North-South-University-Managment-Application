    void deleteRow() {
        try {
            JPopupMenu pop = new JPopupMenu();
            JMenuItem item = new JMenuItem("Delete");
            item.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    DefaultTableModel m = (DefaultTableModel) jTable1.getModel();
                    //m.removeRow(jTable1.getSelectedRow());
                    Connection con = new DB().getConnect();
                    try {
                        PreparedStatement stm = con.prepareStatement("delete from result where id=" + m.getValueAt(jTable1.getSelectedRowCount(), 0) + "");
                        int a = stm.executeUpdate();
                        if (a == 1) {
                            JOptionPane.showMessageDialog(null, "Successfully delete");
                            removerow();
                            resultshow();
                        }
                    } catch (SQLException ex) {
                        Logger.getLogger(Result.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            });
            pop.add(item);
            jTable1.setComponentPopupMenu(pop);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

