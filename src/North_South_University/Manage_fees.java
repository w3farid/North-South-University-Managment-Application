     void resultshow() {
        Connection con = new DB().getConnect();
        DefaultTableModel m = (DefaultTableModel) teacher_table.getModel();

        try {
            PreparedStatement stm = con.prepareStatement("select * from fees;");
            ResultSet rs = stm.executeQuery();
            while (rs.next()) {
                m.addRow(new Object[]{rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getInt(5), rs.getInt(6), rs.getInt(7)});
            }
        } catch (SQLException ex) {
            Logger.getLogger(Result.class.getName()).log(Level.SEVERE, null, ex);
        }

    }
