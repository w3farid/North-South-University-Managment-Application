    private void idShow() {
        Connection con = new DB().getConnect();
        try {
            DefaultTableModel m = (DefaultTableModel) teacher_table.getModel();
            PreparedStatement stm = con.prepareStatement("select * from students WHERE department like ?");
            stm.setString(1, (String) jComboBox1.getSelectedItem());
            ResultSet rs = stm.executeQuery();
            while (rs.next()) {
                jComboBox5.addItem(rs.getString(1));
            }

        } catch (SQLException ex) {
            ex.printStackTrace();
        }
    }

    boolean deleteI(String a) {
        int b = 0;
        DefaultTableModel m = (DefaultTableModel) teacher_table.getModel();
        if (m.getRowCount() > 0) {
            for (int i = 0; i < m.getRowCount(); i++) {
                if (a.matches((String) m.getValueAt(i, 0))) {
                    b = 0;
                    break;
                } else {
                    b = 1;
                }
            }
        } else {
            b = 1;
        }
        if (b == 0) {
            return false;
        } else {
            return true;
        }

    }

