    public void filter(String sql) {
        DefaultTableModel m = (DefaultTableModel) Employee_table.getModel();
        TableRowSorter<DefaultTableModel> tr = new TableRowSorter<DefaultTableModel>(m);
        Employee_table.setRowSorter(tr);
        tr.setRowFilter(RowFilter.regexFilter(sql));
    }

