    void deleteRow() {
        try {
            JPopupMenu pop = new JPopupMenu();
            JMenuItem item = new JMenuItem("Delete");
            item.addActionListener(new ActionListener() {
                @Override
                public void actionPerformed(ActionEvent e) {
                    DefaultTableModel m = (DefaultTableModel) Employee_table.getModel();
                    m.removeRow(Employee_table.getSelectedRow());
                }
            });
            pop.add(item);
            Employee_table.setComponentPopupMenu(pop);
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e);
        }

    }

    void showData() {
        Connection con = new DB().getConnect();
        try {
            DefaultTableModel m = (DefaultTableModel) Employee_table.getModel();
            PreparedStatement stm = con.prepareStatement("SELECT * FROM employee");
            ResultSet rs = stm.executeQuery();
            
            while (rs.next()) {
                m.addRow(new Object[]{rs.getString(1), rs.getString(2), rs.getString(3), rs.getString(4), rs.getString(5), rs.getString(6), rs.getString(7),
                    rs.getString(8), rs.getString(9), rs.getString(10), rs.getString(10), rs.getString(12), rs.getString(13), rs.getString(14), rs.getString(15), rs.getString(16), rs.getString(17), rs.getString(18), rs.getString(19), rs.getString(20), rs.getString(21), rs.getString(22), rs.getString(23), rs.getString(24), rs.getString(25)});
            }
            stm.close();
            con.close();
        } catch (SQLException ex) {
            Logger.getLogger(Employees.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }

