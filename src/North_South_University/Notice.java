    private void jTextField2KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField2KeyReleased
        try {
            Connection con = new DB().getConnect();

            PreparedStatement stm = con.prepareStatement("select * from notice");
            ResultSet rs = stm.executeQuery();
            while (rs.next()) {
                if (jTextField2.getText().matches(rs.getString(1))) {
                    jTextField1.setText(rs.getString(1));
                    jTextArea1.setText(rs.getString(2));
                    jButton3.setEnabled(true);

                }
            }

        } catch (SQLException ex) {
            Logger.getLogger(Notice.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jTextField2KeyReleased

