    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            Connection con=new DB().getConnect();
            PreparedStatement stm=con.prepareStatement("update lb_books set book_name=? where book_id=?");
            stm.setInt(2, Integer.parseInt(jTextField1.getText()));
            stm.setString(1, jTextField2.getText());
           int result=stm.executeUpdate();
           if(result>0){
               JOptionPane.showMessageDialog(null, "Edit Successfully completed");
           }else{
                JOptionPane.showMessageDialog(null, "Not completed");
           }
            
        } catch (SQLException ex) {
            JOptionPane.showMessageDialog(null, ex);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        try {
            Connection con=new DB().getConnect();
            PreparedStatement stm=con.prepareStatement("Select * from lb_books where book_id="+Integer.parseInt(jTextField1.getText())+"");
            ResultSet rs=stm.executeQuery();
            if(rs.next()){
                jTextField2.setText(rs.getString(2));
                jTextField2.setEnabled(true);
            }
            
        } catch (Exception e) {
            if(jTextField1.getText().length()<1){
             jTextField2.setText("");
             jTextField2.setEnabled(false);
         }
        }
        
    }//GEN-LAST:event_jTextField1KeyReleased

