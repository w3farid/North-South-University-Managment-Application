    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        try {
            boolean re = jTextArea1.print();
            if (re) {
                Connection con = null;
                CallableStatement cstm = null;
                DefaultTableModel m = (DefaultTableModel) jTable1.getModel();
                con = new DB().getConnect();
                int a = 0;
                for (int i = 0; i < m.getRowCount(); i++) {
                    try {
                        cstm = con.prepareCall("{call insert_lb_purchase(?,?,?,?,?,?,?,?)}");
                        cstm.setInt(1, Integer.parseInt((String) m.getValueAt(i, 0)));
                        cstm.setInt(2, Integer.parseInt((String) m.getValueAt(i, 1)));
                        cstm.setString(3, (String) m.getValueAt(i, 2));
                        cstm.setString(4, (String) m.getValueAt(i, 3));
                        cstm.setString(5, (String) m.getValueAt(i, 4));
                        cstm.setDate(6, Date.valueOf((String) m.getValueAt(i, 5)));
                        cstm.setDouble(7, Double.parseDouble((String) m.getValueAt(i, 6)));
                        cstm.setInt(8, Integer.parseInt((String) m.getValueAt(i, 7)));
                        a = cstm.executeUpdate();
                        if (a == 1) {
                            a = 1;
                        } else {
                            a = 0;
                            break;
                        }
                    } catch (Exception e) {
                        JOptionPane.showMessageDialog(null, e);
                    } finally {

                    }
                }
                if (a > 0) {
                    JOptionPane.showMessageDialog(null, "Successfully completed");
                } else {
                    JOptionPane.showMessageDialog(null, "Invalid");
                }
            } else {
                JOptionPane.showMessageDialog(null, "Sure Cancel");
            }
        } catch (PrinterException ex) {
            JOptionPane.showMessageDialog(null, ex);
            ex.printStackTrace();
        }
    }//GEN-LAST:event_jButton4ActionPerformed

