    private void jTextField1KeyReleased(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_jTextField1KeyReleased
        DefaultTableModel m=(DefaultTableModel) jTable1.getModel();
        for(int i=0;i<m.getRowCount();i++){
            if(m.getValueAt(i, 0).equals(jTextField1.getText())){
                jComboBox1.setSelectedItem(m.getValueAt(i, 1));
                jTextField3.setText((String) m.getValueAt(i, 2));
                jTextField4.setText((String) m.getValueAt(i, 3));
                jTextField5.setText((String) m.getValueAt(i, 4));
                jTextField6.setText((String) m.getValueAt(i, 5));
                jTextField7.setText((String) m.getValueAt(i, 6));
                jTextField8.setText((String) m.getValueAt(i, 7));
            }
        }
        
    }//GEN-LAST:event_jTextField1KeyReleased

    private void jButton6ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton6ActionPerformed
        DefaultTableModel m=(DefaultTableModel) jTable1.getModel();
        for(int i=0;i<m.getRowCount();i++){
            if(m.getValueAt(i, 0).equals(jTextField1.getText())){
                m.setValueAt(jTextField1.getText(), i, 0);
                m.setValueAt(jComboBox1.getSelectedItem(), i, 1);
                m.setValueAt(jTextField3.getText(), i, 2);
                m.setValueAt(jTextField4.getText(), i, 3);
                m.setValueAt(jTextField5.getText(), i, 4);
                m.setValueAt(jTextField6.getText(), i, 5);
                m.setValueAt(jTextField7.getText(), i, 6);
                m.setValueAt(jTextField8.getText(), i, 7);
            }
        }
    }//GEN-LAST:event_jButton6ActionPerformed

