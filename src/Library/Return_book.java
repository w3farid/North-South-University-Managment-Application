    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
            int rid=Integer.parseInt(jTextField1.getText());
       int bid=Integer.parseInt((String) jComboBox1.getSelectedItem());
       String bname=jTextField3.getText();
       String d=jTextField6.getText();
       Date date=Date.valueOf(d);
       int qty=Integer.parseInt(jTextField8.getText());
        boolean re=AllMethod.returnBook(rid, bid , bname, date , qty);
        if(re==true){
            ImageIcon icon=new ImageIcon("src\\Library\\iamge\\ok.png");
            
            JOptionPane.showMessageDialog(null, "Successfully added", "Return Books", 0, icon);
        }else{
            ImageIcon icon2=new ImageIcon("src\\Library\\iamge\\delete.png");
            JOptionPane.showMessageDialog(null, "Duplicate entry", "Return Books", 0, icon2);
        }
        } catch (Exception e) {
        }
    }//GEN-LAST:event_jButton1ActionPerformed

