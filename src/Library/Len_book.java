    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        try {
             int lid = Integer.parseInt(jTextField1.getText());
        int bid = Integer.parseInt((String) jComboBox1.getSelectedItem());
        String b_name = jTextField3.getText();
        int sid = Integer.parseInt(jTextField7.getText());
       
        String name = jTextField4.getText();
        String dprt = jTextField5.getText();
        String d = jTextField6.getText();
        Date date=Date.valueOf(d);
        int qty = Integer.parseInt(jTextField8.getText());
        boolean a = AllMethod.lenBook(lid, bid,b_name, sid, name, dprt, date, qty);
        if(a==true){
            ImageIcon icon=new ImageIcon("src\\Library\\iamge\\ok.png");
            
            JOptionPane.showMessageDialog(null, "Successfully added", "Return Books", 0, icon);
        }else{
            ImageIcon icon2=new ImageIcon("src\\Library\\iamge\\delete.png");
            JOptionPane.showMessageDialog(null, "Duplicate entry", "Return Books", 0, icon2);
        }
        } catch (Exception e) {
             ImageIcon icon2=new ImageIcon("src\\Library\\iamge\\delete.png");
            JOptionPane.showMessageDialog(null, "Blank not allow", "Return Books", 0, icon2);
        }
    }//GEN-LAST:event_jButton1ActionPerformed

